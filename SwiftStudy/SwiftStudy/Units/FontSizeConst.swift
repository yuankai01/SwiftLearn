import UIKit

func kFontSize(_ size : CGFloat) -> UIFont {
    return UIFont.systemFont(ofSize: size)
}

func KFontBoldSize(_ size: CGFloat) -> UIFont {
    return UIFont.boldSystemFont(ofSize: size)
}

